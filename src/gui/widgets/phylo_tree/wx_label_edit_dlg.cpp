void CwxLabelEditDlg::SetParams(CPhyloTree* tree,
                                CPhyloTree::TTreeIdx node_idx)
{
    m_Tree = tree;
    m_NodeIdx = node_idx; 
    m_Label.erase();

    m_Label = ToWxString((*tree)[node_idx]->GetLabel());
}

