    itemButton5->SetDefault();
    itemStdDialogButtonSizer4->AddButton(itemButton5);

    itemStdDialogButtonSizer4->Realize();

    // Set validators
    itemTextCtrl3->SetValidator( wxTextValidator(wxFILTER_NONE, & m_Label) );
